import java.util.Vector;


public class Vertx {
	private String name;
	private int index;
	
	public Vertx(String name, int ind)
	{
		this.name = name;
		this.index = ind;
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
	
	
	
}
